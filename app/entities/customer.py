class Customer:
    def __init__(self, educationLevel: str, occupation: str, homeOwner: bool, customerKey: int, prefix: str, firstName: str, lastName: str, birthDate: str, maritalStatus: str, gender: str, emailAddress: str, annualIncome: float, totalChildren: int):
        self.educationLevel = educationLevel
        self.occupation = occupation
        self.homeOwner = homeOwner
        self.customerKey = customerKey
        self.prefix = prefix
        self.firstName = firstName
        self.lastName = lastName
        self.birthDate = birthDate
        self.maritalStatus = maritalStatus
        self.gender = gender
        self.emailAddress = emailAddress
        self.annualIncome = annualIncome
        self.totalChildren = totalChildren
